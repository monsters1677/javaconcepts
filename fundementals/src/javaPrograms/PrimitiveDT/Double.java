package fundementals.src.javaPrograms.PrimitiveDT;

public class Double {
    public static void main(String[] args){
        int v=6085;
        double d=(double) v;
        System.out.println("The integer value 'v' is after casting into double ="+d);
        double f=63673.5d;
        int j=(int) f;
        System.out.println("The double value 'f' is after casting into integer ="+j);
    }
}
