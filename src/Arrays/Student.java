package Arrays;

public class Student {
    String stid;
    String stName;
    long stPhoneNo;
    String stEmail;
    String stFeeStatus;

    public Student(String stid, String stName, long stPhoneNo, String stEmail, String stFeeStatus){
        this.stid=stid;
        this.stName=stName;
        this.stPhoneNo=stPhoneNo;
        this.stEmail=stEmail;
        this.stFeeStatus=stFeeStatus;
    }
}
